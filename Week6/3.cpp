void draw(const vector<string>& image, vector<string>& canvas, int x, int y)
{
    for (int i = 0; i < CHAR_HEIGHT; ++i)
    {
        for (int j = 0; j < CHAR_WIDTH - 1; ++j)
        {
            if (image[i][j] == '#')
            {
                if (y + i < canvas.size() && x + j < canvas[0].size())
                {
                    canvas[y + i][x + j] = '#';
                }
            }
        }
    }
}

void draw(const string& text, vector<string>& canvas, int x, int y)
{
    for (size_t i = 0; i < text.size(); ++i)
    {
        char c = text[i];
        if (c >= 'A' && c <= 'Z')
        {
            draw(FONT[c - 'A'], canvas, x + i * CHAR_WIDTH, y);
        }
        else if (c >= '0' && c <= '9')
        {
            draw(FONT[26 + (c - '0')], canvas, x + i * CHAR_WIDTH, y);
        }
    }
}

void displayCanvas(const vector<string>& canvas)
{
    for (const auto& line : canvas)
    {
        cout << line << '\n';
    }
}

const int WIDTH = 60;
const int HEIGHT = 10;
string s;

int main()
{
    cin >> s;
    vector<string> canvas(HEIGHT, string(WIDTH, ' '));
    for (int i = 0; i < s.size(); i++)
    {
        if ('a' <= s[i] && s[i] <= 'z') s[i] -= 32;
    }
    draw(s.c_str(), canvas, 1, 1);
    displayCanvas(canvas);
    return 0;
}
